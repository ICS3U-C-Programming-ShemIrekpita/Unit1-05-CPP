#include <iostream>
#include <cmath>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 5cm and width 3cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 5 * 3 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (5 + 3) << " cm\n";
}
